# Convert data to a list of tuples (connections)
def generate_grid_map(rows, cols):
    grid_map = {}
    for row in range(1, rows + 1):
        for col in range(1, cols + 1):
            grid_num = (row - 1) * cols + col
            connections = []
            if row > 1:  # Up
                connections.append(grid_num - cols)
            if row < rows:  # Down
                connections.append(grid_num + cols)
            if col > 1:  # Left
                connections.append(grid_num - 1)
            if col < cols:  # Right
                connections.append(grid_num + 1)
            grid_map[grid_num] = connections
    return grid_map

